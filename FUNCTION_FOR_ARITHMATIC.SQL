CREATE OR REPLACE FUNCTION ARITHMETIC(N1 NUMBER, N2 NUMBER, SYMBOL CHAR) 
RETURN NUMBER
AS
BEGIN
	CASE SYMBOL
		WHEN '+' THEN RETURN N1 + N2;
		WHEN '-' THEN RETURN N1 - N2;
		WHEN '*' THEN RETURN N1 * N2;
		WHEN '/' THEN RETURN N1 / N2;
	END CASE;
END;
/

--> QUERY

SELECT ARITHMETIC(10,20,'+') FROM DUAL;
SELECT ARITHMETIC(&N1,&N2,'OPERATORS') FROM DUAL;
