DECLARE
    N NUMBER;
BEGIN
     N:='&NUMBER';
	 
	IF N=1 THEN
	    DBMS_OUTPUT.PUT_LINE('MONDAY');
	ELSIF N=2 THEN
	    DBMS_OUTPUT.PUT_LINE('TUESDAY');
	ELSIF N=3 THEN
	    DBMS_OUTPUT.PUT_LINE('WEDNESDAY');
	ELSIF N=4 THEN
	    DBMS_OUTPUT.PUT_LINE('THURSDAY');
	ELSIF N=5 THEN
	    DBMS_OUTPUT.PUT_LINE('FRIDAY');
	ELSIF N=6 THEN
	    DBMS_OUTPUT.PUT_LINE('SATURDAY');
	ELSIF N=7 THEN
	    DBMS_OUTPUT.PUT_LINE('SUNDAY');
	END IF;
END;