DECLARE
	N NUMBER;
BEGIN
	N:=&NUMBER;
	
	IF N=1 THEN
	DBMS_OUTPUT.PUT_LINE('MONDAY');
	
	ELSIF N=2 THEN
	DBMS_OUTPUT.PUT_LINE('TUESDAY');
	
	ELSIF N=3 THEN
	DBMS_OUTPUT.PUT_LINE('WEDDAY');
	
	ELSIF N=4 THEN
	DBMS_OUTPUT.PUT_LINE('THUDAY');
	
	ELSIF N=5 THEN
	DBMS_OUTPUT.PUT_LINE('FRIDAY');
	
	ELSIF N=6 THEN
	DBMS_OUTPUT.PUT_LINE('SATDAY');
	
	ELSE 
	DBMS_OUTPUT.PUT_LINE('SUNDAY');
	
	END IF;
END;
/